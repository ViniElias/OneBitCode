let entrada1 = prompt("Digite o primeiro valor:");
let entrada2 = prompt("Digite o segundo valor:");

valor1 = parseFloat(entrada1);
valor2 = parseFloat(entrada2);

const adicao = valor1 + valor2;
const subtracao = valor1 - valor2;
const multiplicacao = valor1 * valor2;
const divisao = valor1 / valor2;

alert("Adição: " + adicao + "\nSubtração: " + subtracao + "\nMultiplicação: " + multiplicacao + "\nDivisão: " + divisao);
